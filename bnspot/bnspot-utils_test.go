func TestParseDepth20(t *testing.T) {
	depth20Str := []byte(`{"stream":"flmusdt@depth20@100ms","data":{"lastUpdateId":165284515,"bids":[["0.48560000","2036.02000000"],["0.48550000","480.00000000"],["0.48520000","14257.67000000"],["0.48510000","1056.25000000"],["0.48500000","1894.32000000"],["0.48480000","2145.67000000"],["0.48460000","2196.59000000"],["0.48330000","3000.00000000"],["0.48320000","2531.26000000"],["0.48310000","21.18000000"],["0.48300000","4292.54000000"],["0.48270000","5042.00000000"],["0.48240000","5051.00000000"],["0.48230000","24.83000000"],["0.48220000","457.11000000"],["0.48200000","4142.12000000"],["0.48160000","31.15000000"],["0.48150000","71.96000000"],["0.48130000","1284.94000000"],["0.48110000","1098.85000000"]],"asks":[["0.48630000","5601.00000000"],["0.48650000","990.00000000"],["0.48670000","7816.00000000"],["0.48680000","7914.96000000"],["0.48690000","963.00000000"],["0.48720000","3640.00000000"],["0.48730000","814.24000000"],["0.48780000","3560.00000000"],["0.48800000","1029.00000000"],["0.48880000","13221.24000000"],["0.48940000","3000.00000000"],["0.48980000","62.75000000"],["0.49000000","1482.94000000"],["0.49040000","516.34000000"],["0.49110000","46.50000000"],["0.49120000","27.10000000"],["0.49130000","31.03000000"],["0.49150000","66.27000000"],["0.49160000","1291.65000000"],["0.49190000","159.76000000"]]}}`)
	depth, err := ParseDepth20(depth20Str)
	if err != nil {
		t.Fatal(err)
	}
	stream := Depth20Stream{}
	err = json.Unmarshal(depth20Str, &stream)
	if err != nil {
		t.Fatal(err)
	}
	s := strings.Split(stream.Stream, "@")
	assert.Equal(t, strings.ToUpper(s[0]), depth.Symbol)
	assert.Equal(t, stream.Data.LastUpdateId, depth.LastUpdateId)
	for i := 0; i < 20; i++ {
		assert.Equal(t, stream.Data.Asks[i][0], depth.Asks[i][0])
		assert.Equal(t, stream.Data.Asks[i][1], depth.Asks[i][1])
		assert.Equal(t, stream.Data.Bids[i][0], depth.Bids[i][0])
		assert.Equal(t, stream.Data.Bids[i][1], depth.Bids[i][1])
	}
}

