package starkex

import (
	"github.com/stretchr/testify/assert"
	"testing"
)

func TestPedersenParamsInit (t *testing.T) {

	assert.NotNil(t, PedersenParams)

	assert.Equal(t, "3618502788666131213697322783095070105623107215331596699973092056135872020481", PedersenParams.FieldPrime.String())
	assert.Equal(t, "3618502788666131213697322783095070105526743751716087489154079457884512865583", PedersenParams.EcOrder.String())
	assert.Equal(t, "3141592653589793238462643383279502884197169399375105820974944592307816406665", PedersenParams.Beta.String())

	assert.Equal(t, "2089986280348253421170679821480865132823066470938446095505822317253594081284", PedersenParams.ConstantPoints[0][0].String())
	assert.Equal(t, "1713931329540660377023406109199410414810705867260802078187082345529207694986", PedersenParams.ConstantPoints[0][1].String())

	assert.Equal(t, "1254733481274108825174693797237617285863727098996450904398879255272288617861", PedersenParams.ConstantPoints[505][0].String())
	assert.Equal(t, "2644890941682394074696857415419096381561354281743803087373802494123523779468", PedersenParams.ConstantPoints[505][1].String())

}
